# Files used
files_list = c("1-100","101-120","121-160","161-200","201-250","251-300",
              "301-350","351-400","401-450","451-500","501-550","551-600",
              "601-650","651-700","701-725","726-750","751-775","776-800",
              "801-825","826-850","851-900","901-1100","1101-1130","1131-1160",
              "1161-1190","1191-1217")
files_seq = list(1:100,101:120,121:160,161:200,201:250,251:300,301:350,351:400,
              401:450,451:500,501:550,551:600,601:650,651:700,701:725,726:750,
              751:775,776:800,801:825,826:850,851:900,901:1100,1101:1130,
              1131:1160,1161:1190,1191:1217)

# Read all files
time_mat = data.frame(matrix(nrow = 1217, ncol = 0)) 
for (i in i:length(files_list)) {
  file = read.csv(paste0("multilines_data/unformated_data/all_lines/ped_time/otp_tot_",files_list[i],".csv"))
  file = file[unlist(files_seq[i])]
  time_mat = cbind(time_mat, file)
}

# Save results
write.table(time_mat,"multilines_data/unformated_data/all_lines/ped_time_tot.csv", sep=",", row.names = F,col.names = F)
